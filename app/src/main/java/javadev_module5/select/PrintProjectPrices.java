package javadev_module5.select;

public class PrintProjectPrices {

    private String name;
    private int price;

    public PrintProjectPrices(String name, int price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }
}
