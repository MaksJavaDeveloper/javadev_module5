package javadev_module5.select;

public class FindMaxSalaryWorker {

    private String name;
    private int salary;

    public FindMaxSalaryWorker(String name, int salary) {
        this.name = name;
        this.salary = salary;
    }

    public String getName() {
        return name;
    }

    public int getSalary() {
        return salary;
    }
}
